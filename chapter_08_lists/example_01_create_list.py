# Example 1: Creating Lists
# Real life: A shopping list — multiple items in one place!

# A list of fruits
fruits = ["apple", "banana", "cherry", "mango", "grape"]

# A list of numbers
scores = [95, 87, 76, 92, 88]

# An empty list (we'll add to it later)
my_wishlist = []

print("Fruits:", fruits)
print("Scores:", scores)
print("My wishlist:", my_wishlist)

# Lists can hold different types
mixed_bag = ["Aryan", 9, True, 4.5, "Cricket"]
print("Mixed bag:", mixed_bag)

# How many items?
print(f"Number of fruits: {len(fruits)}")
print(f"Number of scores: {len(scores)}")
