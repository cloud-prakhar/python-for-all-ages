# Example 5: Booleans (True or False)
# Booleans can ONLY be True or False. That's it!
# Real life: Light switch — it's either ON (True) or OFF (False)!

is_sunny = True
is_raining = False
loves_ice_cream = True
has_homework = False

print("Is it sunny?", is_sunny)
print("Is it raining?", is_raining)
print("Does she love ice cream?", loves_ice_cream)
print("Do I have homework?", has_homework)

# Booleans are answers to YES/NO questions!
am_i_human = True
am_i_a_robot = False
print("Am I human?", am_i_human)
print("Am I a robot?", am_i_a_robot)
