# Example 3: Floats (Decimal Numbers)
# Floats have a decimal point.
# Real life: Prices at a store, your height, temperature on a thermometer!

price_of_chocolate = 25.50
height_in_feet = 3.8
body_temperature = 98.6
pi = 3.14159

print("The chocolate costs Rs.", price_of_chocolate)
print("My height is", height_in_feet, "feet.")
print("Normal body temperature is", body_temperature, "F")
print("Pi is approximately", pi)
