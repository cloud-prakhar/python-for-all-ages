# Example 1: Creating Variables
# A variable is like a labeled box that stores information.
# Real life: Your name tag at school!

name = "Riya"          # Storing text
age = 9                # Storing a number
city = "Delhi"         # Storing text

# Now let's use them!
print(name)
print(age)
print(city)

# You can also change a variable (like erasing and rewriting your name tag)
age = 10               # Riya had a birthday!
print(age)             # Now it shows 10
