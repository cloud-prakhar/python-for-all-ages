# Example 2: Integers (Whole Numbers)
# Integers are counting numbers — no decimals!
# Real life: Counting your crayons, cookies, or friends!

crayons = 24
friends = 5
cookies = 12
score = 100

print("I have", crayons, "crayons.")
print("I have", friends, "friends.")
print("I have", cookies, "cookies.")
print("My score is", score, "out of 100!")

# Integers can be negative too
temperature = -3
print("It is", temperature, "degrees outside. Brrr!")
