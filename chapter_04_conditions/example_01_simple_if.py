# Example 1: Simple If Statement
# Real life: If I'm hungry, I'll eat a snack.

is_hungry = True

if is_hungry:
    print("Time to eat a snack!")  # This runs because is_hungry is True

is_tired = False

if is_tired:
    print("Go take a nap.")        # This does NOT run because is_tired is False

print("Done checking!")            # This always runs
