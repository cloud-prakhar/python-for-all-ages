# Example 3: elif — Multiple Conditions
# Real life: Choosing what to wear based on temperature.
# "elif" means "Else If" — check ANOTHER condition if the first wasn't True.

temperature = 15   # Change this and run again!

if temperature >= 35:
    print("It's very hot! Wear shorts and drink water. 🥵")
elif temperature >= 25:
    print("It's warm. A t-shirt is perfect! 😊")
elif temperature >= 15:
    print("It's a bit cool. Wear a light jacket. 🧥")
elif temperature >= 5:
    print("It's cold! Wear a sweater and scarf. 🧣")
else:
    print("It's freezing! Stay indoors! ❄️")
