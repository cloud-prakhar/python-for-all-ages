# Example 2: If and Else
# Real life: If it's raining, take an umbrella. Else, wear sunglasses.

weather = "rainy"

if weather == "rainy":
    print("Take your umbrella! ☔")
else:
    print("Wear your sunglasses! 😎")

# Another example: Is the number even or odd?
number = 7

if number % 2 == 0:
    print(f"{number} is an EVEN number.")
else:
    print(f"{number} is an ODD number.")
