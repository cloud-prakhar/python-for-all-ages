# Example 1: Addition
# Real life: You have 5 apples. Your friend gives you 3 more. How many total?

my_apples = 5
friend_apples = 3
total_apples = my_apples + friend_apples

print(f"I had {my_apples} apples.")
print(f"My friend gave me {friend_apples} more.")
print(f"Now I have {total_apples} apples!")
