# Example 4: Comparison Operators
# These always give True or False — like answering YES or NO!
# Real life: Is your score higher than your friend's? Is it equal?

my_score = 85
friend_score = 90

print(f"My score: {my_score}, Friend's score: {friend_score}")
print()

print(f"Is my score equal to friend's?    {my_score == friend_score}")
print(f"Is my score NOT equal to friend's? {my_score != friend_score}")
print(f"Is my score greater?              {my_score > friend_score}")
print(f"Is my score less?                 {my_score < friend_score}")
print(f"Is my score at least 85?          {my_score >= 85}")
print(f"Is friend's score at most 90?     {friend_score <= 90}")
