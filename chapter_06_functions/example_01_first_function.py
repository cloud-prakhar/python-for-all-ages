# Example 1: Your First Function
# Real life: Writing a recipe once and using it whenever you need it!

# DEFINE the function (write the recipe)
def say_hello():
    print("Hello there!")
    print("Welcome to Python!")
    print("Functions are awesome!")

# CALL the function (use the recipe)
say_hello()

print("---")

# Call it again — same function, used twice!
say_hello()
