# Example 1: String Basics
# Real life: Your name, address, favorite song — all strings!

name = "Python Programmer"

print(name)
print(len(name))           # How many characters (including space)?
print(name[0])             # First character
print(name[-1])            # Last character
print("Python" in name)    # Is "Python" inside the string?
print("Java" in name)      # Is "Java" inside? No!
